use axum::http::HeaderMap;
use serde::Serialize;

// ─── Language enum ────────────────────────────────────────────────────────────

#[derive(Debug, Clone, Copy, PartialEq)]
pub enum Lang {
    En,
    Fr,
    Es,
    Ru,
}

impl Lang {
    pub fn code(&self) -> &'static str {
        match self {
            Lang::En => "en",
            Lang::Fr => "fr",
            Lang::Es => "es",
            Lang::Ru => "ru",
        }
    }

    pub fn from_code(code: &str) -> Self {
        match code {
            "fr" => Lang::Fr,
            "es" => Lang::Es,
            "ru" => Lang::Ru,
            _ => Lang::En,
        }
    }

    /// BCP-47 locale string for <html lang="…">
    pub fn html_lang(&self) -> &'static str {
        match self {
            Lang::En => "en",
            Lang::Fr => "fr",
            Lang::Es => "es",
            Lang::Ru => "ru",
        }
    }
}

// ─── Detection ────────────────────────────────────────────────────────────────

/// 1. Cookie `imgpact-lang` (user preference) → 2. Accept-Language header → 3. English default
pub fn detect_lang(headers: &HeaderMap) -> Lang {
    // Cookie override
    if let Some(cookie_hdr) = headers.get("cookie").and_then(|v| v.to_str().ok()) {
        for segment in cookie_hdr.split(';') {
            let s = segment.trim();
            if let Some(val) = s.strip_prefix("imgpact-lang=") {
                return Lang::from_code(val.trim());
            }
        }
    }
    // Accept-Language
    if let Some(accept) = headers.get("accept-language").and_then(|v| v.to_str().ok()) {
        let a = accept.to_lowercase();
        for token in a.split(',') {
            let tag = token.split(';').next().unwrap_or("").trim();
            if tag.starts_with("fr") { return Lang::Fr; }
            if tag.starts_with("es") { return Lang::Es; }
            if tag.starts_with("ru") { return Lang::Ru; }
            if tag.starts_with("en") { return Lang::En; }
        }
    }
    Lang::En
}

